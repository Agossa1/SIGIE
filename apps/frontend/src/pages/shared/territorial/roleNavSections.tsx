const FIELD_SECTION: { label: string; pageIds: RolePageId[] } = {
  label: "MISSIONS TERRAIN",
  pageIds: ["dashboard", "fieldOps", "agentReports", "interventions", "teamsGps"],
}

export function buildNavSections(pageIds: RolePageId[]): NavSection[] {
  const hasTerritorial = pageIds.some((id) =>
    ["gisMap", "infrastructure", "roads", "sanitation", "alerts"].includes(id)
  )
  const hasAdmin = pageIds.some((id) =>
    ["users", "organizations", "roles", "access", "auditLog"].includes(id)
  )

  if (!hasTerritorial && !hasAdmin) {
    return [
      {
        label: FIELD_SECTION.label,
        items: FIELD_SECTION.pageIds
          .filter((id) => pageIds.includes(id))
          .map((id) => ({
            id,
            label: PAGE_LABELS[id] ?? ROLE_PAGE_DEFINITIONS[id].title,
            icon: ICONS[id],
          })),
      },
    ]
  }

  return SECTION_DEFS.map((section) => ({
    label: section.label,
    items: section.pageIds
      .filter((id) => pageIds.includes(id))
      .map((id) => ({
        id,
        label: PAGE_LABELS[id] ?? ROLE_PAGE_DEFINITIONS[id].title,
        icon: ICONS[id],
      })),
  })).filter((s) => s.items.length > 0)
}
